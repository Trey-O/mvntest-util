package com.cloudbees.mvntest.util;

public class Math {

    public static int add (int a, int b, int c) {
	return a + b + c;
    }

    public static int subtract (int a, int b) {
	return a - b;
    }
    
    public static int multiply (int a, int b) {
	return a * b;
    }
    
    public static int divide (int a, int b) {
	return a / b;
    }
}